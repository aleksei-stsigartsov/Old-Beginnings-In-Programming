def toMorze(textMessage, mLib=morzeCyrDig):
    adds=['••—••','•———•','•——••','••••——••']
    addsIndex=0
    morzeMessage=''
    for i in textMessage:
        if i in mLib:
            morzeMessage+=mLib[i]+' '
        else:
            morzeMessage+=adds[addsIndex]
            addsIndex+=1
            if addsIndex>3:
                addsIndex=0
    return morzeMessage

